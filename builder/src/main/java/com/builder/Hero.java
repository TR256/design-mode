package com.builder;

public class Hero {

    private String name;
    private String A;

    private String Q;

    private String W;

    private String E;

    private String R;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getA() {
        return A;
    }

    public void setA(String a) {
        A = a;
    }

    public String getQ() {
        return Q;
    }

    public void setQ(String q) {
        Q = q;
    }

    public String getW() {
        return W;
    }

    public void setW(String w) {
        W = w;
    }

    public String getE() {
        return E;
    }

    public void setE(String e) {
        E = e;
    }

    public String getR() {
        return R;
    }

    public void setR(String r) {
        R = r;
    }
}

